    public void obtenerDatos(){
        //Se le asignan los valores puestos en el formulario a las variables globales
        mNombre = F_Nombre.getText();
        mTipo_Nave = (String) F_Tipo_nave.getSelectedItem();
        mTipo_combustible = F_Tipo_combustible.getText();
        mRegion = (String) F_Region.getSelectedItem();
        mPotencia = Double.parseDouble(F_Potencia.getText());
        mEmpuje = Double.parseDouble(F_Empuje.getText());
        mVelocidad = Double.parseDouble(F_Velocidad.getText());
        mPeso = Double.parseDouble(F_Peso.getText());
        mDestino = (String) F_Destino.getSelectedItem();
        mTipo_mision = (String) F_Tipo_mision.getSelectedItem();
        mCapacidad = Integer.parseInt(F_Capacidad.getText());

    }
    
    public void guardarDatos(){
        
        //Se crea un objeto de tipo nave, para que contenga todas las caracteristicas de estas
        Nave nuevaNave = new Nave();
        
        //Se guardan los valores que se pusieron en el formulario en las variables de la clase Nave
        nuevaNave.setNombre(mNombre); 
        nuevaNave.setTipo_nave(mTipo_Nave);
        nuevaNave.setTipo_combustible(mTipo_combustible);
        nuevaNave.setRegion(mRegion);
        nuevaNave.setPotencia(mPotencia);
        nuevaNave.setEmpuje(mEmpuje);
        nuevaNave.setVelocidad(mVelocidad);
        nuevaNave.setPeso(mPeso);
        nuevaNave.setDestino(mDestino);
        nuevaNave.setTipo_mision(mTipo_mision);
        nuevaNave.setCapacidad(mCapacidad);
        
        //Se llama la clase de inicio, que es donde esta la lista global, y se le agrega la nave creada
        Inicio.BD_Naves.add(nuevaNave);
        
        
        //Se pone un mensaje de que se agrego correctamente
        JOptionPane.showMessageDialog(this, "Se guardo la nave " + mNombre + " exitosamente");
        
        
    }
    
