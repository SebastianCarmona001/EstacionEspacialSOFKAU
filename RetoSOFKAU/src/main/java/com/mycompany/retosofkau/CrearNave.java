    private void But_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_But_GuardarActionPerformed
        
        //Se obtienen los datos del formulario
        this.obtenerDatos();
        
        //Se crea un iterador para que busque en la lista BD_Naves
        Iterator<Nave> iterador = Inicio.BD_Naves.iterator();
        
        //Se inicializa el auxiliar en 0
        aux = 0;
        
        //Se itera sobre la lista, que esta metida en el iterador
        while(iterador.hasNext()){
            
            //Se va cambiando de registro mientras exista un siguiente
            Nave Naves = iterador.next();
            
            //Si el nombre de la nave ya se encuentra en la lista, se anuncia que ya existe y se pone el auxiliar en 1
            if (Naves.getNombre().equals(mNombre)){
                JOptionPane.showMessageDialog(this, "La nave " + mNombre + " Ya existe en el sistema");
                aux = 1;
            }
        }
        
        //Si el auxiliar no cambio, entonces se guarda la informacion
        if(aux == 0){
            this.guardarDatos();
        }
        
        //Se limpia el formulario
        this.Limpiar_Formulario();
        
        //Se pone el auxiliar en 0 otra vez
        aux = 0;
    }//GEN-LAST:event_But_GuardarActionPerformed

    
    
